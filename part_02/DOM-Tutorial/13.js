// insertBefore;
// The JavaScript insertBefore () is a method that is used for inserting a child node before another node of a specified parent node.

// The insertBefore() method of the Node interface inserts a node before a reference node as a child of a specified parent node. If the given node already exists in the document, insertBefore() moves it from its current position to the new position

// replaceChild;
// replaceChild() method to replace an HTML element by a new one. To replace an HTML element, you use the node.replaceChild() method: parentNode.replaceChild(newChild, oldChild); In this method, the newChild is the new node to replace the oldChild node which is the old child node to be replaced.


// removeChild
