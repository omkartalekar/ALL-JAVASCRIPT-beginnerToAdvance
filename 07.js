// trim 
//The trim() method removes whitespace from both sides of a string. The trim() method does not change the original string.
firstName="  omkar  ";
console.log(firstName.trim()); //omkar


// toUpperCase()
// toUpperCase() method returns a string without any lowercase characters
firstName="omkar";
console.log(firstName.toUpperCase()); //OMKAR


// toLowerCase()
// toLowerCase() method returns a string without any uppercase letters.
firstName="OMKAR";
console.log(firstName.toLowerCase()); 
// omkar

// slice()
// The slice() method returns selected elements in an array, as a new array. The slice() method selects from a given start, up to a (not inclusive) given end.

console.log(firstName.slice(0,3)); //OMK

